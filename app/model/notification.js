const mongoose = require("mongoose");

const notificationSchema = mongoose.Schema(
  {
    userid: {
      type: Number,
      required: true,
    },
    title: {
      type: String,
      required: true,
    },
    message: {
      type: String,
      required: true,
    },
    type: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

module.exports = mongoose.model("notifications", notificationSchema);
